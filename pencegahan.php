    <td width="144" valign="top"><p align="center">Disleksia </p></td>
    <td width="299" valign="top"><p>Disleksia adalah  sebuah gangguan dalam perkembangan baca-tulis yang umumnya terjadi pada anak  menginjak usia 7 hingga 8 tahun. Ditandai dengan kesulitan belajar membaca  dengan lancar dan kesulitan dalam memahami meskipun normal atau diatas  rata-rata.</p></td>
    <td width="299" valign="top"><p>Pengobatan untuk disleksia dengan menggunakan metode  terapi. dan Terapi yang bisa Anda gunakan untuk mengatasi gangguan disleksia  ini adalah, Terapi Binaural Beats Dyslexia.</p>
      Terapi Binaural Beats  Dyslexia merupakan sebuah terapi yang sudah teruji untuk membantu Anda untuk  bisa meminimalisir serta menyembuhkan Anda dari gangguan disleksia. Terapi  Binaural Beats Dyslexia ini berbentuk audio dan dicetak dalam bentuk CD, dengan  tujuan agar Anda bisa dengan mudah untuk mendapatkan dan menggunakannya</td>
