    <td width="144" valign="top"><p align="center">ADHD (Attention Deficit Hyperactivity Disorder)</p></td>
    <td width="299" valign="top"><p>ADHD (Attention Deficit Hyperactivity Disorder)  adalah gangguan  perkembangan dalam  peningkatan aktivitas motorik anak-anak hingga menyebabkan  aktivitas anak-anak yang tidak lazim dan cenderung berlebihan. </p></td>
