    <td width="144" valign="top"><p align="center">Skizofrenia</p></td>
    <td width="299" valign="top"><p>Skizofrenia  adalah penyakit gangguan mental, ditandai dengan pola pikir yang tidak teratur,  delusi, halusinasi, perubahan perilaku yang tidak tepat serta adanya gangguan  psikososial. Gangguan skizofrenia berawal dari keluhan halusinasi dan waham  yang khas seperti mendengar pikirannya sendiri dan diucapkan dengan nada yang  keras.</p></td>
    <td width="299" valign="top"><p>Pengobatan untuk Skizofrenia, antara  lain: dilakukannya  pemeriksaan mental yang menyeluruh dari penderita yaitu pemeriksaan fisik,  neurologi, riwayat keluarga dan kehidupan sosial penderita serta melakukan terapi seperti pelatihan untuk kemampuan sosial dan kemampuan kerja. serta dukung psikologi dan pendidikan untuk anak.</p>
    </td>
